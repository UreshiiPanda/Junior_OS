//
// Decrement the reference count on a page,
// freeing it if there are no more refs.
//
void
page_decref(struct PageInfo* pp)
{
	if (--pp->pp_ref == 0)
		page_free(pp);
}

// Given 'pgdir', a pointer to a page directory, pgdir_walk returns
// a pointer to the page table entry (PTE) for linear address 'va'.
// This requires walking the two-level page table structure.
//
// The relevant page table page might not exist yet.
// If this is true, and create == false, then pgdir_walk returns NULL.
// Otherwise, pgdir_walk allocates a new page table page with page_alloc.
//    - If the allocation fails, pgdir_walk returns NULL.
//    - Otherwise, the new page's reference count is incremented,
//	the page is cleared,
//	and pgdir_walk returns a pointer into the new page table page.
//
// Hint 1: you can turn a PageInfo * into the physical address of the
// page it refers to with page2pa() from kern/pmap.h.
//
// Hint 2: the x86 MMU checks permission bits in both the page directory
// and the page table, so it's safe to leave permissions in the page
// directory more permissive than strictly necessary.
//
// Hint 3: look at inc/mmu.h for useful macros that manipulate page
// table and page directory entries.
//
pte_t *
pgdir_walk(pde_t *pgdir, const void *va, int create)
{
	// Fill this funtion in
    //
    // we are using va to find our page table entry
    // first we will use the first 10 bits of va to get the idx into the page dir
    // then we use the next 10 bits to get the page table entry in that page dir
    // and that is what this func returns, so it took in a va and returned the
    // associated pa (a pointer to it) via the page tables
    //
    // so we use virtual addresses to get to the entry in the page dir/tables,
    // and then the entry itself is the pa
    //

    // get first 10 bits via PDX macro, then get PDE by indexing this info in
    // the pgdir[] arr
    pde_t pde = pgdir[PDX(va)];

    //  cprintf("pgdir_walk, here is pde&PTE_P: %d, here is create: %d, and here is both: %d\n\n", pde&PTE_P, create, pde&PTE_P && create);
    // if the page table DNE and we are not creating a page, then return NULL
    if (!pde&PTE_P && !create) {
	    return NULL;
    } else if (pde&PTE_P) {
        // page is valid and we are NOT creating it
        // so we grab this page from the page table entry, but this is a phys
        // address, so we also need to convert it so that the kernel can actually
        // use it
        // cprintf("in pgdir_walk, returning exisiting page\n\n");
        pte_t *page_table = (pte_t *)KADDR(PTE_ADDR(pde));
        // and now we have the page table, and we will idx into it to get the
        // exact entry in that table we want, and since we are returning a ptr
        // to that entry then we use & also
        return &page_table[PTX(va)];
    } else if (!pde&PTE_P && create) {
        // we are creating the page table
        // so first we alloc a new page
        // cprintf("in pgdir_walk, creating new page\n\n");
        struct PageInfo *pp_page_table = page_alloc(1);
        // if there are no more free pages left, page_alloc returns NULL
        if (pp_page_table == NULL) {
            return NULL;
        }
        // be sure to increment the pp_ref of that table now
        pp_page_table->pp_ref += 1;
        // and now we need to set this page table in the page dir
        // and this is a Phys address, so we convert from virtual
        pgdir[PDX(va)] = page2pa(pp_page_table) | PTE_P | PTE_U | PTE_W;
        // and we return a ptr to that entry holding the phys address
        pde_t pde = pgdir[PDX(va)];
        pte_t *page_table = (pte_t *)KADDR(PTE_ADDR(pde));
        return &page_table[PTX(va)];
    }
    // cprintf("in pgdir_walk, this should NEVER print, you are calling pgdir_walk with both a valid page and with the create flag set\n\n");
    return NULL;
}

//
// Map [va, va+size) of virtual address space to physical [pa, pa+size)
// in the page table rooted at pgdir.  Size is a multiple of PGSIZE, and
// va and pa are both page-aligned.
