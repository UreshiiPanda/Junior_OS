// If pte_store is not zero, then we store in it the address
// of the pte for this page.  This is used by page_remove and
// can be used to verify page permissions for syscall arguments,
// but should not be used by most callers.
//
// Return NULL if there is no page mapped at va.
//
// Hint: the TA solution uses pgdir_walk and pa2page.
//
struct PageInfo *
page_lookup(pde_t *pgdir, void *va, pte_t **pte_store)
{
	// Fill this function in
    //
    // return the pa page mapped to va and store it in pte_store

    // storing into pte_store:
    // first get the ptr to the entry
    pte_t *p_pte = pgdir_walk(pgdir, va, 0);
    if (p_pte == NULL) {
        // if no pa page is mapped at this va, return NULL
        return NULL;
    }
    // then store it
    if (pte_store != NULL) {
        *pte_store = p_pte;
    }
    //  and return the page we found at va, which is a struct PageInfo

    // cprintf("at end of page_lookup()");
    return pa2page(PTE_ADDR(*p_pte));

}

