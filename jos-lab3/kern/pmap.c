// --------------------------------------------------------------
// Tracking of physical pages.
// The 'pages' array has one 'struct PageInfo' entry per physical page.
// Pages are reference counted, and free pages are kept on a linked list.
// --------------------------------------------------------------

//
// Initialize page structure and memory free list.
// After this is done, NEVER use boot_alloc again.  ONLY use the page
// allocator functions below to allocate and deallocate physical
// memory via the page_free_list.
//
void
page_init(void)
{
	// The example code here marks all physical pages as free.
	// However this is not truly the case.  What memory is free?
	//  1) Mark physical page 0 as in use.
	//     This way we preserve the real-mode IDT and BIOS structures
	//     in case we ever need them.  (Currently we don't, but...)
	//  2) The rest of base memory, [PGSIZE, npages_basemem * PGSIZE)
	//     is free.
	//  3) Then comes the IO hole [IOPHYSMEM, EXTPHYSMEM), which must
	//     never be allocated.
	//  4) Then extended memory [EXTPHYSMEM, ...).
	//     Some of it is in use, some is free. Where is the kernel
	//     in physical memory?  Which pages are already in use for
	//     page tables and other data structures?
	//
	// Change the code to reflect this.
	// NB: DO NOT actually touch the physical memory corresponding to
	// free pages!
    //
    //
    // pages is an arr of struct PageInfo
    // pp_link is PageInfo's ptr to the next avail PageInfo page in the LL
    // pp_ref is a PageInfo's count of how many refs are using that phys page
    // NOTE: pp_ref only counts refs that were allocated with page_alloc(), and
    // refs allocated via boot_alloc() were already setup, and boot_alloc()
    // returned the next_free_page so this is where we start our free-page
    // structure
    //
    // leave a NULL node at the TAIL of the LL, so "page_free_list" will always
    // be the HEAD of the LL, and NULL will always be the tail of the LL, and
    // we will insert pages btw these always
    //
    //
    //
    // first we need to check (for all pages in the pages arr) which of these
    // pages are already being used by the Qs 1-4 above. So these pages's pp_ref
    // will be set to 1 if they are already in use, or set to 0 otherwise. You
    // will then have an arr of all pages with their appropriate reference num.
    // THEN, you use this code below (and you need to add in the if statement
    // which will check if page[i] has pp_ref=0 or not), to construct the LL
    // of avail phys pages (so only add a phys page to the LL if it is free)
    //
    //

    // panic("page_init() now starting");

    // the tail of page_free_list should be a NULL dummy node
   //  struct PageInfo tail;
   //  tail.pp_ref = 1;
   //  tail.pp_link = NULL;
   //  page_free_list = &tail;
    page_free_list = NULL;
	size_t i;
    unsigned long pages_init_bytes = (unsigned long)&pages - (unsigned long)KERNBASE;

	for (i = 0; i < npages; i++) {
        // if this page at i in the arr has no more refs, then put it at the
        // head of the LL. So we iterate thru all of the pages in the arr
        // to construct the LL
        // KERNBASE + 4MB is at 0xF4000000
        // cprintf("in page_init, idx is at i=%d\n\n", i);
        if (i == 0) {
            // cprintf("pages: %p\n\n", pages);
            pages[i].pp_ref = 1;
            // cprintf("page_init() page i=0 set");

        } else if (i < npages_basemem) {
		    pages[i].pp_ref = 0;
        } else if (i >= npages_basemem && i < npages_basemem + (384000/PGSIZE)) {
            // we are now at (npages_basemem * PGSIZE bytes)
            // and IOPHYSMEM to EXTPHYSMEM is a 384KB hole reserved for devices
            // and we need to divide this by PGSIZE to get the num of pages
		    pages[i].pp_ref = 1;
        } else if (i >= npages_basemem + (384000/PGSIZE) && i < npages_basemem + (384000/PGSIZE) + (size_t)(pages_init_bytes/PGSIZE)) {
            // we are now at 0x100000 hex and need to decide addresses above
            // this are already in use based on boot_alloc, etc.
            // so this is all phys mem above 1MB, this is where the kernel was
            // placed by mem_init, so now we have the next_free_page var which
            // was returned by boot_alloc and then set to *pages in mem_init
            // and everything from that page and above is free extmem
            //
            // the kernel is 4MB in size (KERNBASE + 4MB) so we need to divide
            // this by the PGSIZE to get the num of pages it uses up
            //
            // above the kernel is the struct PageInfo *pages arr which was
            // made by boot_alloc, but some of this was used, and so the next
            // available page is returned by boot_alloc and is stored by
            // mem_init in the pages* var. So everything btw the end of the
            // kernel and the address of *pages is being used
		    pages[i].pp_ref = 1;
        } else {
            // now all of the mem above the *pages var is free
		    pages[i].pp_ref = 0;
        }
        // now insert the pages that had no refs
        // we only insert curr phys page into the LL if it is free
        if (pages[i].pp_ref == 0) {
            // we always insert after the HEAD
            // so point this curr page to the HEAD.next
	    	pages[i].pp_link = page_free_list;
            // then move the HEAD pointer to now point at this new HEAD.next
	    	page_free_list = &pages[i];
        }
	}

    // cprintf("END OF PAGE_INIT:   here is page_free_list: %p and its value: %p, and its pp_link: %p\n\n", page_free_list, *page_free_list, page_free_list->pp_link);
    // cprintf("page_init() has worked i think, now leaving page_init()\n\n");
}

