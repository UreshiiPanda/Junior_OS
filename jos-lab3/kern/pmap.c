//
// Map the physical page 'pp' at virtual address 'va'.
// The permissions (the low 12 bits) of the page table entry
// should be set to 'perm|PTE_P'.
//
// Requirements
//   - If there is already a page mapped at 'va', it should be page_remove()d.
//   - If necessary, on demand, a page table should be allocated and inserted
//     into 'pgdir'.
//   - pp->pp_ref should be incremented if the insertion succeeds.
//   - The TLB must be invalidated if a page was formerly present at 'va'.
//
// Corner-case hint: Make sure to consider what happens when the same
// pp is re-inserted at the same virtual address in the same pgdir.
// However, try not to distinguish this case in your code, as this
// frequently leads to subtle bugs; there's an elegant way to handle
// everything in one code path.
//
// RETURNS:
//   0 on success
//   -E_NO_MEM, if page table couldn't be allocated
//
// Hint: The TA solution is implemented using pgdir_walk, page_remove,
// and page2pa.
//


int
page_insert(pde_t *pgdir, struct PageInfo *pp, void *va, int perm)
{

    //cprintf("IN PAGE INSERT: here is to-be-inserted page: %p, and it's value: %p, and pp_ref: %d, and pp_link: %p\n\n", pp, *pp, pp->pp_ref, pp->pp_link);
    //cprintf("PAGE INSERT, page_free_list: %p, pp_ref: %d, pp_link: %p\n\n", page_free_list, page_free_list->pp_ref, page_free_list->pp_link);
    // first remove the pa that this va is already mapped to if there is one
    // if there isn't one, page_remove does nothing
    //cprintf("PAGE INSERT    VA    %p\n\n", va);
    //cprintf("PAGE INSERT    Page    %p,   page2pa   %p\n\n", pp, page2pa(pp));
    // page_remove(pgdir, va);
    // get the pa that the input wants to map this va to
    physaddr_t pa = page2pa(pp);
    // get the PTE at this va, or create one if this va has no mapping already
    pte_t *p_pte = pgdir_walk(pgdir, va, 1);
    //cprintf("JUST REMOVED new pa: %p, and old pa: %p, and pte: %p\n\n", pa, PTE_ADDR(*p_pte), p_pte);
    if (p_pte == NULL) {
        // either the va given to pgdir walk DNE or out of mem
        return -E_NO_MEM;
    } else if (PTE_ADDR(*p_pte) == pa) {
        // the pa in the pp that was just mapped to va was already mapped to
        // this va, so we don't add this mapping and just return, but we still
        // want to be able to change perms without increasing the ref count
        *p_pte = PTE_ADDR(page2pa(pp)) | perm | PTE_P;
        return 0;
    } else if (*p_pte & PTE_P) {
        // this va is already mapped to another pa, so that pa should first be
        // removed from this va. Cuz a va cannot map to multiple pa's
        // struct PageInfo *old_page = pa2page(PTE_ADDR(*p_pte));
        //void *old_va = page2kva(old_page);
        //cprintf("old va: %p\n\n", old_va);
        page_remove(pgdir, va);
        // old_page->pp_ref -= 1;
        //*p_pte = 0;
        // page_decref(old_page);
        //tlb_invalidate(pgdir, va);
    }

    // we get the pa pointed to by the page-pointer pp via page2pa(pp)
    // and we use this to point our p_pte pointer to this pa, along with
    // the permissions
    *p_pte = PTE_ADDR(page2pa(pp)) | perm | PTE_P;
    pp->pp_ref += 1;
    return 0;

}


